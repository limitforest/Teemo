	/**
	 * 对数据进行过滤，如将负数调整为正数，去掉低于某一指定值的数
	 * @param dd 数据
	 * @return 经过过滤的新的数据
	 */
	private int[] dataFilter(byte[] dd) {
		int len = dd.length;
		int[] newD = new int[len];
		
		
		int lower = pref.getInt("lower", 0); //下限值
		//int[] lowers = {0,15,31,47,63,79,95,111,127,143,159,175,191,207,223,239,255,256};
		//int lower = lowers[2];
		for(int i = 0;i<len;i++){
			int data = dd[i];
			//将负数调整为正数
			if (data < 0)
				data += 256;
			if(data<lower)
				data = 0;
			newD[i] = data;
		}
		return newD;
	}

	
	
	

